package com.programs;

public class Pattern3 {

	public static void main(String[] args) {
		int row = 4;
		int clm = 4;

      for(int i=1; i<=row; i++)
      {
    	  for(int j=1; j<=clm; j++)
    	  {
    		  System.out.print(j+" ");
    	  }
    	  System.out.println();
      }
	}

}


